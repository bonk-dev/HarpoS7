    [Test]
    public void Lut1()
    {
        ReadOnlySpan<byte> arg1Bytes = new byte[16]
        {
            0x91, 0x4F, 0xA2, 0xB1, 0xAE, 0xA0, 0x5B, 0xA6, 0x54, 0x8D, 0x1F, 0x24,
            0x2C, 0xA6, 0x01, 0x24
        };
        Span<uint> arg1 = new uint[4];
        for (int i = 0; i < arg1.Length; ++i)
        {
            arg1[i] = BinaryPrimitives.ReadUInt32LittleEndian(arg1Bytes[(i * 4)..]);
        }

        //Span<byte> output = new byte[16];
        Span<uint> output = new uint[4];

        HarpoHash.Lut1(output, arg1);

        ReadOnlySpan<byte> expectedBytes = new byte[16]
        {
            0x48, 0xA7, 0xD1, 0x58, 0xD7, 0x50, 0x2D, 0xD3, 0x2A, 0x46, 0x8F, 0x92,
            0x16, 0x53, 0x00, 0x92
        };
        Span<uint> expected = new uint[4];
        for (int i = 0; i < arg1.Length; ++i)
        {
            expected[i] = BinaryPrimitives.ReadUInt32LittleEndian(expectedBytes[(i * 4)..]);
        }

        for (int i = 0; i < expected.Length; ++i)
        {
            Assert.That(output[i], Is.EqualTo(expected[i]));
        }
    }

